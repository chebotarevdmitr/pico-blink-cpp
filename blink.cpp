// НАЗНАЧЕНИЕ: Мигание встроенным светодиодом на Raspberry Pi Pico
// ЯЗЫК: C++ (совместим с C, так как Pico SDK использует C-подобный API)
// ============================================================================

// 1️⃣ ПОДКЛЮЧЕНИЕ ЗАГОЛОВОЧНЫХ ФАЙЛОВ (HEADERS)
// В C++ мы подключаем "инструкции" по использованию готовых функций.
// Файл pico/stdlib.h содержит базовые функции Pico: работа с пинами, задержки, инициализация.
# include"pico/stdlib.h"

// 2️⃣ ОПРЕДЕЛЕНИЕ ПОСТОЯННОЙ (CONSTANT)
// const означает: "это значение нельзя изменить во время работы программы".
// 25 — это номер GPIO-пина, к которому подключён встроенный светодиод на большинстве плат Pico.
// (На некоторых клонах это 25, на других может быть 13 или другой. Оставим 25 как стандарт).
const int LED_PIN = 25;

// 3️⃣ ТОЧКА ВХОДА В ПРОГРАММУ
// Любая C/C++ программа для микроконтроллера начинает выполнение с функции main().
// int main(void) означает: "эта функция возвращает целое число (int) и не принимает аргументов (void)".
// На микроконтроллерах return 0; обычно не выполняется, но синтаксис требует его наличия.
int main(void)
{

  // 🔹 ШАГ А: ИНИЦИАЛИЗАЦИЯ БАЗОВЫХ ФУНКЦИЙ SDK
  // Эта функция подготавливает системные часы, настраивает базовые таймеры и GPIO.
  // Без неё функции вроде gpio_init() или sleep_ms() могут работать некорректно.
  stdio_init_all();

  // 🔹 ШАГ Б: НАСТРОЙКА КОНКРЕТНОГО ПИНА (GPIO)
  // gpio_init() "будит" пин и переводит его из энергосберегающего режима в рабочий.
  // Представь: пин спал, мы его аккуратно растолкали.
  gpio_init(LED_PIN);

  // gpio_set_dir() указывает, как пин будет использоваться:
  // GPIO_OUT — пин будет выдавать сигнал (включать/выключать светодиод)
  // (Есть ещё GPIO_IN — для чтения кнопок или датчиков, но сейчас не нужно)
  gpio_set_dir(LED_PIN, GPIO_OUT);

  // 🔹 ШАГ В: БЕСКОНЕЧНЫЙ ЦИКЛ (MAIN LOOP)
  // Микроконтроллеры не "завершают" работу. Они крутятся в цикле, пока есть питание.
  // while(1) или while(true) — стандартный паттерн для встраиваемых систем.
  while (true)
  {

    // Включаем светодиод: подаём на пин HIGH (логическая 1, ~3.3V)
    gpio_put(LED_PIN, 1);

    // Ждём 500 миллисекунд (0.5 секунды)
    // sleep_ms() использует аппаратные таймеры RP2040, поэтому CPU может "отдыхать"
    sleep_ms(500);

    // Выключаем светодиод: подаём на пин LOW (логический 0, 0V)
    gpio_put(LED_PIN, 0);

    // Снова ждём 0.5 секунды
    sleep_ms(500);
  }

  // Эта строка теоретически не достижима из-за while(true),
  // но синтаксис C++ требует, чтобы main() что-то возвращал.
  return 0;
}